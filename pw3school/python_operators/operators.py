# Python Identity Operators

"""
Identity operators are used to compare the objects, not if they are equal,  but if they are actual the same
object, with the same memory location
"""

# is
# is not
x = ["Hello world"]
y = ["Hello world"]
z = x

print(x is y)  # False
print(x is z)  # True

