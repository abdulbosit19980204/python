--PostgreSQL Introduction

--What is PostgreSQL?

PostgreSQL is an free and open-source database system that supports both relational (SQL) and non-relational (JSON)
queries.

PostgreSQL is a back-end database for dynamic websites and web applications.
PostgreSQL supports the most important programming languages; Python, Java ... Tcl

--PostgreSQL supports basically all features that other database management system support

PostgreSQL History
PostgreSQL was invited at the Berkeley Computer Science Department, University of California.

It started as a project in 1986 with the goal of creating a database system with the minimal features needed to support
multiple data types.

In the beginning, PostgreSQL ran on Unix platforms, but now it can run on various platforms, including Windows and MacOS
